// slide item 1

function showInfo1() {
  document.querySelector("#info-box1").style.display = "flex";
  document.querySelector("#clickContainer").style.display = "none";
  document.querySelector("#navigate-area").style.display = "none";
}

function hideInfo1() {
  document.querySelector("#info-box1").style.display = "none";
  document.querySelector("#clickContainer").style.display = "flex";
  document.querySelector("#navigate-area").style.display = "flex";
}

